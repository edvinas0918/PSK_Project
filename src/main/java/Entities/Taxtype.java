package Entities;

import java.io.Serializable;
import java.util.List;
import javax.persistence.Basic;
import javax.persistence.CascadeType;
import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.FetchType;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.NamedQueries;
import javax.persistence.NamedQuery;
import javax.persistence.OneToMany;
import javax.persistence.Table;
import javax.validation.constraints.NotNull;
import javax.validation.constraints.Size;
import javax.xml.bind.annotation.XmlRootElement;
import javax.xml.bind.annotation.XmlTransient;

/**
 *
 * @author Dziugas
 */
@Entity
@Table(name = "taxtype")
@NamedQueries({
    @NamedQuery(name = "Taxtype.findAll", query = "SELECT t FROM Taxtype t"),
    @NamedQuery(name = "Taxtype.findById", query = "SELECT t FROM Taxtype t WHERE t.id = :id"),
    @NamedQuery(name = "Taxtype.findByName", query = "SELECT t FROM Taxtype t WHERE t.name = :name")})
@XmlRootElement
public class Taxtype implements Serializable {

    private static final long serialVersionUID = 1L;
    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    @Basic(optional = false)
    @Column(name = "ID")
    private Integer id;
    @Basic(optional = false)
    @NotNull
    @Size(min = 1, max = 40)
    @Column(name = "Name")
    private String name;
    @OneToMany(orphanRemoval = true, mappedBy = "taxTypeID", fetch = FetchType.EAGER)
    private List<Tax> taxList;

    public Taxtype() {
    }

    public Taxtype(Integer id) {
        this.id = id;
    }

    public Taxtype(Integer id, String name) {
        this.id = id;
        this.name = name;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @XmlTransient
    public List<Tax> getTaxList() {
        return taxList;
    }

    public void setTaxList(List<Tax> taxList) {
        this.taxList = taxList;
    }

    @Override
    public int hashCode() {
        int hash = 0;
        hash += (id != null ? id.hashCode() : 0);
        return hash;
    }

    @Override
    public boolean equals(Object object) {
        // TODO: Warning - this method won't work in the case the id fields are not set
        if (!(object instanceof Taxtype)) {
            return false;
        }
        Taxtype other = (Taxtype) object;
        if ((this.id == null && other.id != null) || (this.id != null && !this.id.equals(other.id))) {
            return false;
        }
        return true;
    }

    @Override
    public String toString() {
        return "Entities.Taxtype[ id=" + id + " ]";
    }
    
}
